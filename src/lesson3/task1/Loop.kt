 */
fun lcm(m: Int, n: Int): Int {
    var nod = 0
    var nok = 0
    var k = m
    var l = n
    while ((k != 0) && (l != 0)) {
        if (k > l) k = k % l
        else l = l % k




    }
    nod = k + l
    nok = m * n / nod
    return nok

}

