fun squareSequenceDigit(n: Int): Int {
    var k = 1
    var p = 0
    var j = 0
    for (i in 1..(n +1)) {
        p = digitNumber(i * i)
        j += p
        if (j >= n) {
            k = i * i
            break
        }

    }
    if (j == n) return k % 10
    else {
        while (j != n) {
            k = k / 10
            j = j - 1
        }
        return k % 10
    }
}

